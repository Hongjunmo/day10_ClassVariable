package day10_ClassVariable;

public class Ex01_Class {

	public int sum(int n, int n1) {

		return n + n1;

	}
	
	public int multisum(int n, int n1,int n2) {
		
		return n + n1+n2;
		
	}

	
	public double sum(int n, double n2) {
		
		return n + n2;
		
	}
	public String sum(String n, String n2) {
		
		return n + n2;
		
	}

	
	
	
	
}
